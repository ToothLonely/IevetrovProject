package org.example.lesson_1_10.lesson_3

fun main() {
    //Первый ход белых
    var start: String = "E2"
    var finish: String = "E4"
    var countMove: Int = 1

    println("$start-$finish;$countMove")

    //Второй ход белых
    start = "D2"
    finish = "D3"
    countMove++

    println("$start-$finish;$countMove")
}